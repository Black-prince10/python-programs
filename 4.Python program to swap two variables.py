'''
Variable swapping:
In computer programming, swapping two variables specifies the
mutual exchange of values of the variables. It is generally done by using a temporary variable.
'''
print("Python variable swap program:")
x=input("Enter the value of x: ")
y=input("Enter the value of y: ")

#create a temporary variable and swap the values 	
temp =x 
x=y 
y=temp

print("The temparory value of swapping :",x)
print("The temporary value of swapping :",y)














